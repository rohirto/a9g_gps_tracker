uint8_t SPI_WriteReadByte(SPI_ID_t spi, uint8_t data)
{
	uint8_t r;
	SPI_Write(spi, &data, 1);
	while(!SPI_IsTxDone(spi));
	SPI_Read(spi, &r, 1);
	return r;
}

//向SSD1106写入一个字节。
//dat:要写入的数据/命令
//cmd:数据/命令标志 SSD1306_CMD,表示命令;SSD1306_DATA,表示数据;
void SSD1306_WriteByte(uint8_t data, uint8_t cmd)
{	
	if(cmd == SSD1306_CMD)
	    SSD1306_DC_CMD();
	else 
	    SSD1306_DC_DATA();		  
	SPI_WriteReadByte(ssd1306_spi,data);
	SSD1306_DC_CMD();   	  
} 

//清屏函数,清完屏,整个屏幕是黑色的!和没点亮一样!!!	  
void SSD1306_Clear(void)  
{  
	uint8_t i,n;		    
	for(i=0;i<8;i++)  
	{  
		SSD1306_WriteByte (0xb0+i,SSD1306_CMD);    //设置页地址（0~7）
		SSD1306_WriteByte (0x00,SSD1306_CMD);      //设置显示位置—列低地址
		SSD1306_WriteByte (0x10,SSD1306_CMD);      //设置显示位置—列高地址   
		for(n=0;n<128;n++)SSD1306_WriteByte(0,SSD1306_DATA); 
	} //更新显示
}

//开启SSD1306显示    
void SSD1306_DisplayOn(void)
{
	SSD1306_WriteByte(0X8D,SSD1306_CMD);  //SET DCDC命令
	SSD1306_WriteByte(0X14,SSD1306_CMD);  //DCDC ON
	SSD1306_WriteByte(0XAF,SSD1306_CMD);  //DISPLAY ON
}

//关闭SSD1306显示     
void SSD1306_DisplayOff(void)
{
	SSD1306_WriteByte(0X8D,SSD1306_CMD);  //SET DCDC命令
	SSD1306_WriteByte(0X10,SSD1306_CMD);  //DCDC OFF
	SSD1306_WriteByte(0XAE,SSD1306_CMD);  //DISPLAY OFF
}	


void SSD1306_SetPos(unsigned char x, unsigned char y) 
{ 
	SSD1306_WriteByte(0xb0+y,SSD1306_CMD);
	SSD1306_WriteByte(((x&0xf0)>>4)|0x10,SSD1306_CMD);
	SSD1306_WriteByte((x&0x0f)|0x01,SSD1306_CMD); 
}  

//在指定位置显示一个字符,包括部分字符
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示				 
//size:选择字体 16/12 
void SSD1306_ShowChar(uint8_t x,uint8_t y,uint8_t chr, uint8_t size)
{      	
	unsigned char c=0,i=0;	
    c=chr-' ';//得到偏移后的值			
    if(x>ssd1306_width-1){x=0;y=y+2;}
    if(size ==16)
    {
        SSD1306_SetPos(x,y);	
        for(i=0;i<8;i++)
            SSD1306_WriteByte(F8X16[c*16+i],SSD1306_DATA);
        SSD1306_SetPos(x,y+1);
        for(i=0;i<8;i++)
            SSD1306_WriteByte(F8X16[c*16+i+8],SSD1306_DATA);
    }
    else {	
        SSD1306_SetPos(x,y+1);
        for(i=0;i<6;i++)
            SSD1306_WriteByte(F6x8[c][i],SSD1306_DATA);
    }
}

/**
  * display BMP picture
  * 
  * @param x0,x1: BMP pic horizon bits, value from 0 to 127 in 128x64 display module
  *               e.g.: 48x48(x*y)(horizon*virtival) --> x0,x1 = 0,47
  * @param x1,y1: BMP pic virtical pages(every 8 bits one page), value from 0 to 7 in 128x64 display module
  *               e.g.: 48x48(x*y)(horizon*virtival) --> {y0,y1 = 0,(48/8-1) ==> 0,5}
  */
void SSD1306_DrawBMP(unsigned char x0, unsigned char x1, unsigned char y0, unsigned char y1,unsigned char BMP[])
{ 	
    unsigned int j=0;
    unsigned char x,y;

    for(y=y0;y<=y1;y++)
    {
        SSD1306_SetPos(x0,y);
        for(x=x0;x<=x1;x++)
        {      
            SSD1306_WriteByte(BMP[j++],SSD1306_DATA);
        }
    }
} 


/**
  * 
  * display flush all screen
  * 
  * @param data: data to display, length: SSD1306_MAX_DATA_BYTES
  */
void SSD1306_Show(uint8_t* data)
{
    uint16_t i,j;
    
    for(i=0;i<ssd1306_height/8;++i)
    {
        SSD1306_SetPos(0,i);
        SSD1306_DC_DATA();
        for(j = 0; j<ssd1306_width; ++j)
            SPI_WriteReadByte(ssd1306_spi,data[i*ssd1306_width+j]);
    }
	SSD1306_DC_CMD();  
}



