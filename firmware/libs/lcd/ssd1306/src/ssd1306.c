{
	unsigned char j=0;
	while (chr[j]!='\0')
	{		SSD1306_ShowChar(x,y,chr[j],size);
			x+=8;
		if(x>120){x=0;y+=2;}
			j++;
	}
}

/**
  * Initialize SSD1306
  * 
  * @param spiN: spi number
  * @param spiConfig: spi config
  * @return bool: return true if init success or false
  */			    
bool SSD1306_Init(uint16_t width, uint16_t height, SPI_ID_t spiN, SPI_Config_t* spiConfig, 
                     GPIO_PIN rst, GPIO_PIN dc)
{ 	
    if(SPI_MAX == spiN || 
       spiConfig == NULL||
       width == 0||
       height == 0)
        return false;
    
    ssd1306_width = width;
    ssd1306_height = height;
    ssd1306_spi = spiN;
    ssd1306_rst = rst;
    ssd1306_dc = dc;

    // init gpio
    GPIO_EnablePower(rst,true);
    GPIO_EnablePower(dc,true);

    GPIO_config_t pin = {
        .mode         = GPIO_MODE_OUTPUT,
        .pin          = rst,
        .defaultLevel = GPIO_LEVEL_LOW
    };

    GPIO_Init(pin);
    pin.pin = dc;
    GPIO_Init(pin);

    //init interface
    SPI_Init(spiN, *spiConfig);

    
    SSD1306_SET_RST(false);
	OS_Sleep(100);
	SSD1306_SET_RST(true);
	OS_Sleep(200);
	SSD1306_SET_RST(false);
					  
	SSD1306_WriteByte(0xAE,SSD1306_CMD);//--turn off oled panel
	SSD1306_WriteByte(0x00,SSD1306_CMD);//---set low column address
	SSD1306_WriteByte(0x10,SSD1306_CMD);//---set high column address
	SSD1306_WriteByte(0x40,SSD1306_CMD);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
	SSD1306_WriteByte(0x81,SSD1306_CMD);//--set contrast control register
	SSD1306_WriteByte(0xCF,SSD1306_CMD); // Set SEG Output Current Brightness
	SSD1306_WriteByte(0xA1,SSD1306_CMD);//--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
	SSD1306_WriteByte(0xC8,SSD1306_CMD);//Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
	SSD1306_WriteByte(0xA6,SSD1306_CMD);//--set normal display
	SSD1306_WriteByte(0xA8,SSD1306_CMD);//--set multiplex ratio(1 to 64)
	SSD1306_WriteByte(0x3f,SSD1306_CMD);//--1/64 duty
	SSD1306_WriteByte(0xD3,SSD1306_CMD);//-set display offset	Shift Mapping RAM Counter (0x00~0x3F)
	SSD1306_WriteByte(0x00,SSD1306_CMD);//-not offset
	SSD1306_WriteByte(0xd5,SSD1306_CMD);//--set display clock divide ratio/oscillator frequency
	SSD1306_WriteByte(0x80,SSD1306_CMD);//--set divide ratio, Set Clock as 100 Frames/Sec
	SSD1306_WriteByte(0xD9,SSD1306_CMD);//--set pre-charge period
	SSD1306_WriteByte(0xF1,SSD1306_CMD);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
	SSD1306_WriteByte(0xDA,SSD1306_CMD);//--set com pins hardware configuration
	SSD1306_WriteByte(0x12,SSD1306_CMD);
	SSD1306_WriteByte(0xDB,SSD1306_CMD);//--set vcomh
	SSD1306_WriteByte(0x40,SSD1306_CMD);//Set VCOM Deselect Level
	SSD1306_WriteByte(0x20,SSD1306_CMD);//-Set Page Addressing Mode (0x00/0x01/0x02)
	SSD1306_WriteByte(0x02,SSD1306_CMD);//
	SSD1306_WriteByte(0x8D,SSD1306_CMD);//--set Charge Pump enable/disable
	SSD1306_WriteByte(0x14,SSD1306_CMD);//--set(0x10) disable
	SSD1306_WriteByte(0xA4,SSD1306_CMD);// Disable Entire Display On (0xa4/0xa5)
	SSD1306_WriteByte(0xA6,SSD1306_CMD);// Disable Inverse Display On (0xa6/a7) 
	SSD1306_WriteByte(0xAF,SSD1306_CMD);//--turn on oled panel
	
	SSD1306_Clear();
	SSD1306_SetPos(0,0); 	
    return true;
}  

